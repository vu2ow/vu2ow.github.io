			<p class="auto-style46">Summary first <o:p></o:p></p>
			<p class="auto-style46">At the outset, I would say that this is a 
			well designed and constructed kit. It should work just like that out 
			of the box as it did for few people. It was not so for me due to my 
			own mistakes. This feedback is more of the mistakes I made – hope 
			others will benefit by not repeating them. Hopefully they could get 
			some tips too. So, first advice – read the description and the 
			circuit first. <o:p></o:p></p>
			&nbsp;<span class="auto-style46">Assembly</span><br />
			<p class="auto-style46">As soon as I saw the size of the board, I 
			was reminded of PC SMPS cabinet. Sure enough, I checked the size and 
			it would fit perfectly. I had a working 250 W junked SMPS lying 
			around, which uses the old TL494. By adjusting the resistor value in 
			PIN 1, I could have obtained up to 15 V in the 12 V line. I thought 
			about mounting bitx over the SMPS board with aluminum sheet as 
			shield in between. I gave up the idea because the heat sinks used in 
			the SMPS were big and ugly. I could have just resized or use a 
			smaller heat sink as we only require 2 Amps maximum. I did not have 
			the patience to resize or replace the heatsink.<o:p></o:p></p>
			<p class="auto-style46">
			<o:p>Pay attention to instructions</o:p></p>
			<p class="auto-style46">I belong to the “cant wait” category. So I 
			did not read the instructions / circuit in enthusiasm to make it 
			work. I did not like the idea of mounting the microphone inside the 
			cabinet and have a toggle switch for rx/tx and wanted to homebrew a 
			hand mic. This is where I started creating problems for myself and 
			to the well-made rig. I took the 12 V DC directly to the hand mic 
			box and used a relay energized by a PTT button to run 12 V to RX 
			normally and to TX when energized. I also brought the speaker line 
			into the box. Added a cheap 6ohm plastic speaker as only that 
			speaker would fit inside the small ptt box. The audio sounds tinny 
			and distorted when I increase the volume by a certain value. <o:p></o:p>
			</p>
			<p class="auto-style46">As far as the MIC goes, I made a grave 
			mistake. Not seeing the circuit properly, I gave a separate bias to 
			the MIC from the 12 Volts in the box. I had also made other errors 
			with added decoupling capacitors. This resulted in high pitch squeal 
			every quarter turn of the 10 turn 100K pot I used for tuning 
			control. <o:p></o:p>
			</p>
			<p class="auto-style46">
			<o:p>Tuning Range</o:p></p>
			<p class="auto-style46">Tuning range is adequate and it covers the 
			entire band. I just centered the 10 turn pot to 7100 Khz by 
			adjusting C57 trimmer, transmitting from my regular transceiver. <o:p></o:p>
			</p>
			<p class="auto-style46">
			<o:p>Sensitivity</o:p></p>
			<p class="auto-style46">Sensitivity – I would give the rig 10/10. 
			With the mic problem and everything I did, it tuned to RTTY signals 
			quite easily. Initially I did not hear voice signals. Once I 
			corrected the mic mistakes, I started receiving phone audio. It 
			sounded very tinny in my speaker and I was able to improve it a 
			little by playing around with the C45A trimmer. The squeals also 
			disappeared after I corrected the mic mistakes. <o:p></o:p></p>
			<p class="auto-style46">Selectivity – Rig effortlessly switched 
			station to station during busy hours. Did not notice any overlaps. <o:p></o:p>
			</p>
			<p class="auto-style46">Frequency counter addition is a jiffy. 
			Thanks for providing expansion facility on the board. <o:p></o:p>
			</p>
			<p class="auto-style46">For measuring forward and reverse power, I 
			ran a 3 inch bit of RG58 from antenna connector to the BNC. Over 
			that, I slid a ferrite toroid with 12 turns of enameled wire to use 
			as a pick up. Later, I tried plain wire also. I did not notice any 
			difference in reception between the two set ups. <o:p></o:p></p>
			<p class="auto-style46">Above two additions are my future projects. <o:p></o:p>
